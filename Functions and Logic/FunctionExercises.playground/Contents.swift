import UIKit

func addTwoParameters(parameterTheFirst: Double, parameterTheSecond: Double) -> Double {
    return parameterTheFirst + parameterTheSecond
}

func subtractSecondParameter(parameterOne: Int, parameterTwo: Int) -> Int {
    return parameterOne - parameterTwo
}

func divideBySecondParameter(numerator: Double, denominator: Double) -> Double {
    return numerator / denominator
}

